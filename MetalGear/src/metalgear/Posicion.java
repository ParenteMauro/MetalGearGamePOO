package metalgear;

public class Posicion {

    private Integer x;
    private Integer y;

    public Posicion(Integer y, Integer x) {

        this.y = y;
        this.x = x;

    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }

}
